package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {
		
		// create a variable: datatype name
		
		// whole numbers
		byte box1= 69;
		short box2= 6969;
		int box3= 69696969;
		long box4= 696969696969696969L;
		
		//decimal numbers
		float container=6.99F;
		double container2=69.99;
		
		// to represent 1 character
		char gender='m';
		char dollar='$';
		
		// to represent true or false (yes or no)
		
		boolean hungry=true;
		boolean sleepy=false;
		
		//how to access value from variables
		
		System.out.println(container2);  // do not use "" to access variables
		
		// access and print values from all variables
		
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(gender);
		System.out.println(dollar);
		System.out.println(hungry);
		System.out.println(sleepy);
		System.out.println(container);
		
		

	}

}
